# FUNCTION 5: List All Existing Cases
# ============================================

def list_recent_cases(limit=5):
    """List recent cases from TheHive"""
    
    response = thehive.get_cases(limit=limit, sort="-createdAt")
    
    if response.status_code == 200:
        cases = response.json()
        print(f"\n📋 Recent Cases (last {len(cases)}):")
        for case in cases:
            print(f"  #{case.get('caseId')}: {case.get('title')} - {case.get('status')}")
        return cases
    else:
        print(f"❌ Failed to fetch cases: {response.text}")
        return []

