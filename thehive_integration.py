# FUNCTION 1: Create Alert from Coral Data
# ============================================

def create_alert_from_coral(github_user, slack_channel, sentry_project, severity=2):
    """Create an alert in TheHive based on Coral query results"""
    
    source_ref = f"coral-{uuid.uuid4().hex[:8]}"
    
    alert = Alert(
        title=f"[Security Trinity] Suspicious Activity from {github_user}",
        description=f"""
Coral cross-source investigation detected activity:

📊 GitHub User: {github_user}
💬 Slack Channel: {slack_channel}
⚠️ Sentry Project: {sentry_project}

This alert was auto-generated by the Security Trinity agent.
Coral joined data across 3 sources in a single SQL query.
""",
        severity=severity,  # 1=Low, 2=Medium, 3=High
        type="coral",
        source="SecurityTrinity",
        sourceRef=source_ref,
        tags=["coral", "cross-source", "auto-generated", github_user],
        tlp=2,  # Amber
        pap=2
    )
    
    response = thehive.create_alert(alert)
    
    if response.status_code == 201:
        alert_id = response.json()["id"]
        print(f"✅ Alert created: {alert_id}")
        return alert_id
    else:
        print(f"❌ Failed to create alert: {response.text}")
        return None

# ============================================
# FUNCTION 2: Add Observables to Alert
# ============================================

def add_observables_to_alert(alert_id, github_user, slack_channel):
    """Add observables (IPs, domains, etc.) to an alert"""
    
    observables = [
        {"dataType": "user", "data": github_user, "tlp": 2},
        {"dataType": "other", "data": slack_channel, "tlp": 2, "message": "Slack channel"},
        {"dataType": "text", "data": f"GitHub user {github_user} showed suspicious activity", "tlp": 2}
    ]
    
    for obs in observables:
        observable = CaseObservable(
            dataType=obs["dataType"],
            data=obs["data"],
            tlp=obs.get("tlp", 2),
            message=obs.get("message", "")
        )
        response = thehive.create_alert_observable(alert_id, observable)
        
        if response.status_code == 201:
            print(f"  📎 Added observable: {obs['dataType']}: {obs['data']}")
        else:
            print(f"  ❌ Failed to add {obs['dataType']}")

# ============================================
# FUNCTION 3: Promote Alert to Case
# ============================================

def promote_alert_to_case(alert_id):
    """Convert an alert into a full case for investigation"""
    
    response = thehive.promote_alert_to_case(alert_id)
    
    if response.status_code == 201:
        case_id = response.json()["id"]
        case_number = response.json()["caseId"]
        print(f"✅ Alert promoted to Case #{case_number} (ID: {case_id})")
        return case_id
    else:
        print(f"❌ Failed to promote alert: {response.text}")
        return None

