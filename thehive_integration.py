# FUNCTION 4: Run Cortex Analyzer on Observable
# ============================================

def run_cortex_analyzer_on_case(case_id, analyzer_name="VirusTotal_GetReport"):
    """Run a Cortex analyzer on observables in a case"""
    
    # First, get the case observables
    response = thehive.get_case_observables(case_id)
    
    if response.status_code != 200:
        print(f"❌ Could not fetch observables")
        return
    
    observables = response.json()
    
    for obs in observables:
        data_type = obs.get("dataType")
        data = obs.get("data")
        
        if data_type == "user":  # Skip user data, not suitable for VirusTotal
            continue
            
        print(f"🔍 Running {analyzer_name} on {data_type}: {data}...")
        
        # Get Cortex analyzer ID
        analyzers = cortex.get_analyzers()
        
        # This requires Cortex API integration
        # See: https://github.com/TheHive-Project/Cortex4Py
        
        print(f"  Analyzer would run on {data}")

